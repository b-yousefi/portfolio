class PersonalInfo {
  constructor(
    fullName,
    role,
    education,
    pic,
    summary,
    lookingFor,
    linkedInAccountUrl,
    githubAccountUrl
  ) {
    this.fullName = fullName;
    this.role = role;
    this.education = education;
    this.pic = pic;
    this.summary = summary;
    this.lookingFor = lookingFor;
    this.linkedInAccountUrl = linkedInAccountUrl;
    this.githubAccountUrl = githubAccountUrl;
  }
}

export default PersonalInfo;
